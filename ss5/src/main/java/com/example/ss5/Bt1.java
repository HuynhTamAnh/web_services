package com.example.ss5;

public class Bt1 {
    //HTTP Request là thông điệp mà client gửi đến server để yêu cầu thực hiện một hành động cụ thể. Một HTTP Request bao gồm bốn thành phần chính:
    //
    //**Method** xác định loại hành động cần thực hiện, bao gồm GET (lấy dữ liệu), POST (gửi dữ liệu mới), PUT (cập nhật dữ liệu), và DELETE (xóa dữ liệu). **URL** chỉ định địa chỉ tài nguyên mà client muốn truy cập, bao gồm protocol, domain, đường dẫn và tham số query.
    //
    //**Headers** chứa thông tin metadata như loại nội dung (Content-Type), phương thức xác thực (Authorization), thông tin trình duyệt (User-Agent), và các cấu hình khác giúp server hiểu và xử lý request đúng cách.
    //
    //**Body** là phần tùy chọn chứa dữ liệu thực tế được gửi kèm request, thường xuất hiện trong POST và PUT để truyền form data, JSON, hoặc file upload. GET và DELETE thường không có body vì chỉ thực hiện truy vấn hoặc xóa dữ liệu.
}
